import Foundation
import UIKit

extension UIColor
{
    class func flatRed() -> UIColor
    {
        return UIColor(red: 234/255, green: 112/255, blue: 97/255, alpha: 1.0)
    }
    
    class func flatBlue() -> UIColor
    {
        return UIColor(red: 214/255, green: 255/255, blue: 246/255, alpha: 1.0)
    }
    
    class func flatGreen() -> UIColor
    {
        return UIColor(red: 171/255, green: 225/255, blue: 136/255, alpha: 1.0)
    }
}
